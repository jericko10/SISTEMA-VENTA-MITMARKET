    private void btnGenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarReporteActionPerformed
        conexionBaseDatos = BaseDatosConexion.getConnection();
        InputStream archivoReporte = null;
        if(txtAño.getText().length()!=0){
        try {
            archivoReporte = new FileInputStream("src/ComponenteReporte/ReporteProductoMasVendido.jrxml");
            Map parametro = new HashMap();
            parametro.put("Anio",txtAño.getText());
            parametro.put("Mes",String.format("%02d",cbomes.getSelectedIndex()+1));
            JasperDesign jasperDesign = JRXmlLoader.load(archivoReporte);
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametro, conexionBaseDatos);
            JasperViewer jasperViewer = new JasperViewer(jasperPrint,false);
            jasperViewer.setVisible(true);
            jasperViewer.setTitle("REPORTE DE PRODUCTO MAS VENDIDOS");
        } catch (JRException ex) {
            ex.printStackTrace();
        } catch (FileNotFoundException ex) {
        }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Ingrese el año...");
        }
    }//GEN-LAST:event_btnGenerarReporteActionPerformed

